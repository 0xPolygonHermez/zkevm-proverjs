        zkasmCode += value.x1 + "n => A\n";
        zkasmCode += value.y1 + "n => B\n";
        zkasmCode += value.x2 + "n => C\n";
        zkasmCode += value.y2 + "n => D\n";
        zkasmCode += value.x3 + "n => E\n";
